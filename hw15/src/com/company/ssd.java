package com.company;

public class ssd {
    int memory;
    int sections;

    public ssd(int memory, int sections) {
        this.memory = memory;
        this.sections = sections;
    }

    public int getMemory() {
        return memory;
    }

    public void setMemory(int memory) {
        this.memory = memory;
    }

    public int getSections() {
        return sections;
    }

    public void setSections(int sections) {
        this.sections = sections;
    }
}
